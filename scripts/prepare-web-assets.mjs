import { access, cp, mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const managedRoot = path.join(repoRoot, 'contracts', 'managed', 'veilpledge');
const generatedRoot = path.join(repoRoot, 'web', 'src', 'generated');
const generatedContractRoot = path.join(generatedRoot, 'veilpledge');
const publicRoot = path.join(repoRoot, 'web', 'public');

async function exists(filePath) {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function requireCompiledContract() {
  const contractModule = path.join(managedRoot, 'contract', 'index.js');
  if (!(await exists(contractModule))) {
    throw new Error('Compact artifacts are missing. Run `npm run compile` before preparing the web app.');
  }
}

async function copyContractModule() {
  await rm(generatedContractRoot, { force: true, recursive: true });
  await mkdir(generatedContractRoot, { recursive: true });

  for (const fileName of ['index.js', 'index.js.map', 'index.d.ts']) {
    const source = path.join(managedRoot, 'contract', fileName);
    if (await exists(source)) {
      await cp(source, path.join(generatedContractRoot, fileName));
    }
  }
}

async function copyZkArtifacts() {
  for (const directory of ['keys', 'zkir']) {
    const destination = path.join(publicRoot, directory);
    await rm(destination, { force: true, recursive: true });
    await mkdir(path.dirname(destination), { recursive: true });
    await cp(path.join(managedRoot, directory), destination, { recursive: true });
  }
}

async function writeDeploymentModule() {
  const deploymentPath = path.join(repoRoot, 'deployments', 'preprod.json');
  const requireDeployment = process.env.REQUIRE_PREPROD_DEPLOYMENT === '1';
  let deployment = {
    network: 'preprod',
    contractAddress: '',
    transactionHash: '',
    blockHeight: 0,
    deployedAt: '',
  };

  if (await exists(deploymentPath)) {
    const parsed = JSON.parse(await readFile(deploymentPath, 'utf8'));
    if (
      parsed.network !== 'preprod' ||
      !/^[0-9a-f]{64}$/i.test(parsed.contractAddress) ||
      !/^[0-9a-f]{64}$/i.test(parsed.transactionHash) ||
      !Number.isSafeInteger(parsed.blockHeight) ||
      parsed.blockHeight <= 0 ||
      typeof parsed.deployedAt !== 'string' ||
      !Number.isFinite(Date.parse(parsed.deployedAt))
    ) {
      throw new Error('deployments/preprod.json is not a valid Preprod deployment record.');
    }
    deployment = parsed;
  } else if (requireDeployment) {
    throw new Error(
      'A production Pages build requires a verified deployments/preprod.json record.',
    );
  }

  await mkdir(generatedRoot, { recursive: true });
  const source = [
    '// Generated by scripts/prepare-web-assets.mjs. Do not edit.',
    `export const PREPROD_DEPLOYMENT = ${JSON.stringify(deployment, null, 2)} as const;`,
    '',
  ].join('\n');
  await writeFile(path.join(generatedRoot, 'deployment.ts'), source, 'utf8');
}

await requireCompiledContract();
await Promise.all([copyContractModule(), copyZkArtifacts(), writeDeploymentModule()]);

process.stdout.write('Prepared browser contract module, ZK artifacts, and public Preprod metadata.\n');
